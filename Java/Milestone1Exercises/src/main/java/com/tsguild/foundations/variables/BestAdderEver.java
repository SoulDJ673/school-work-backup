public class BestAdderEver {
    public static void main(String[] args) {
        
        //Variable Declaration
        int first;
        int second;
        int third;
        int total;
        
        //Program Start
        first = 2;
        second = 5;
        third = 8;
        System.out.println("The value of the first number is: " + first);
        System.out.println("The value of the second number is: " + second);
        System.out.println("The value of the third number is: " + third);
        total = first + second + third;
        System.out.println("They add up to equal: " + total);
        System.out.println("Again, the total is: " + total);
    }
    
}
